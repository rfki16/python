# input data

# data yang diinput pasti string
data = input("Masukan nama anda : ")

print("Nama = ", data, "type : ", type(data))

# data interger dan float
angka = int(input("Masukan angka : "))
float = float(input("Masukan angka : "))

print("Angka = ", angka, "type : ", type(angka))
print("Float = ", float, "type : ", type(float))

# data boolean
biner = bool(int(input("Masukan biner : ")))

print("Biner = ", biner, "type : ", type(biner))
